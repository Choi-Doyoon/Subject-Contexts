package OOP;

public class Example2 {
    public static void main(String[] args){
        int num; //정수형(integer) 변수명: num 선언
        num=100; //변수값으로 100 할당(대입)
        System.out.println("This is num: "+num); //문자열에 num 변수 값 추가(+ 기호)
        num=num*2;
        System.out.print("The value of num*2 is "); //print: 줄바꿈 없이 출력
        System.out.println(num);
    }
}
